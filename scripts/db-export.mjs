/**
 * Exporta todos os dados do banco para JSON
 * N√£o requer pg_dump - usa Prisma
 * 
 * Uso: 
 *   DATABASE_URL="postgresql://..." node scripts/db-export.mjs
 */

import { PrismaClient } from '@prisma/client';
import fs from 'fs';

const prisma = new PrismaClient();

async function exportDatabase() {
  console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
  console.log('   EXPORTANDO BANCO DE DADOS');
  console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

  try {
    // Exportar ve√≠culos
    console.log('üì¶ Exportando ve√≠culos...');
    const vehicles = await prisma.vehicle.findMany();
    console.log(`   ‚úÖ ${vehicles.length} ve√≠culos`);

    // Exportar conversas
    console.log('üì¶ Exportando conversas...');
    const conversations = await prisma.conversation.findMany();
    console.log(`   ‚úÖ ${conversations.length} conversas`);

    // Exportar mensagens
    console.log('üì¶ Exportando mensagens...');
    const messages = await prisma.message.findMany();
    console.log(`   ‚úÖ ${messages.length} mensagens`);

    // Exportar leads
    console.log('üì¶ Exportando leads...');
    let leads = [];
    try {
      leads = await prisma.lead.findMany();
      console.log(`   ‚úÖ ${leads.length} leads`);
    } catch (e) {
      console.log('   ‚ö†Ô∏è  Tabela Lead n√£o existe');
    }

    // Criar objeto de backup
    const backup = {
      exportedAt: new Date().toISOString(),
      version: '1.0',
      data: {
        vehicles,
        conversations,
        messages,
        leads
      },
      counts: {
        vehicles: vehicles.length,
        conversations: conversations.length,
        messages: messages.length,
        leads: leads.length
      }
    };

    // Salvar arquivo
    const timestamp = new Date().toISOString().replace(/[:.]/g, '-').slice(0, 19);
    const filename = `scripts/db-backup-${timestamp}.json`;
    
    fs.writeFileSync(filename, JSON.stringify(backup, null, 2));
    
    console.log('\n‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log(`   ‚úÖ BACKUP SALVO: ${filename}`);
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');

    // Mostrar resumo
    console.log('\nüìä Resumo:');
    console.log(`   Ve√≠culos: ${vehicles.length}`);
    console.log(`   Conversas: ${conversations.length}`);
    console.log(`   Mensagens: ${messages.length}`);
    console.log(`   Leads: ${leads.length}`);

    // Mostrar categorias de ve√≠culos
    const byCategory = vehicles.reduce((acc, v) => {
      acc[v.carroceria || 'N/I'] = (acc[v.carroceria || 'N/I'] || 0) + 1;
      return acc;
    }, {});
    
    console.log('\nüìä Ve√≠culos por categoria:');
    Object.entries(byCategory)
      .sort((a, b) => b[1] - a[1])
      .forEach(([cat, count]) => {
        console.log(`   ${cat}: ${count}`);
      });

    return filename;

  } catch (error) {
    console.error('\n‚ùå Erro ao exportar:', error.message);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

exportDatabase().catch(console.error);

